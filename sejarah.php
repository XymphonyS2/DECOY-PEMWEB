                    <p>Pada tahun 1989 pemerintah menerapkan suatu kebijakan menghapus SPG diseluruh Indonesia, maka SPG Muhammadiyah Kedaton Bandar Lampung, oleh persyarikatan Muhammadiyah dialih fungsikan menjadi SMA Muhammadiyah Putri kedaton Bandar Lampung, berdasarkan hasil studi banding ke SMA Muhammadiyah 2 Putri Yogyakarta. Namun kondisi sosial, ekonomi dan budaya di Propinsi Lampung berbeda dengan kondisi sosial, ekonomi dan budaya di Yogyakarta. SPG Muhammadiyah yang pada Tahun Aajaran 1989/1990, meluluskan siswa sebanyak 5 kelas, ternyata penerimaan kelas I hanya 1 kelas, dengan jumlah siswa sebanyak 20 orang. Melihat respon masyarakat yang kurang begitu antusias untuk mnyekolahkan anaknya di SMA Muhammadiyah Putri, karena di Propinsi Lampung SMA putri belum begitu umum dimasyarakat. Maka melalui keputusan Rapat Majlis Pendidikan dan Kebudayaan Pimpinan Daerah Muhammadiyah Kedaton sebagai lembaga pengelola, diputuskan untuk mengubah SMA Muhammadiyah Putri Kedaton Bandar Lampung, menjadi SMA Muhammadiyah 2 Kedaton Bandar Lampung, hingga saat ini. </p>
