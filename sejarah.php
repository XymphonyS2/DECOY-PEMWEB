                    <h2>Sejarah</h2>
                    <p>SMA Muhammadiyah 2 Bandar Lampung awal berdirinya adalah SPG Muahammadiyah Kedaton Kabupaten Lampung Selatan, dengan alamat Jalan Turi Kampung Sidodadi Kabupaten Lampung Selatan dengan Piagam pendirian Pimpinan Pusat Muhammadiyah MPPK Nomor: 524/II-017/1977 tanggal 6 Dzulhijjah 1397 H bertepatan dengan tanggal 17 November 1977 M. Karena kondisi tanah dan bangunan di Jalan Turi Kampung Sidodadi kurang memadai, maka pada tanggal 7 Februari 1978 SPG Muhammadiyah dipindahkan kekomplek Perguruan Muhammadiyah Labuhanratu Kabupaten Lampung Selatan yang berjarak kurang lebih 2 km dari tempat semula. Sesuai dengan gerak langkah pembangunan Kota Madya Bandar Lampung, dalam program pengembangan/perluasan wilayah, maka SPG Muhammadiyah yang semula masuk dalam wilayah Lampung Selatan masuk kedalam wilayah Kota Madya Bandar Lampung, selanjutnya berubah nama menjadi SPG Muhammadiyah Kedaton Kota Madya Bandar Lampung dengan alamat Jln. Teuku Umar No. 14 Labuhanratu Kedaton Bandar Lampung dengan Kode pos 35142.</p>
